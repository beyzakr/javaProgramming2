package day03_EscapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("/ \\");  // in order to print one backward slash we need to give two backward slashes

        System.out.println("My favorite TV show is \"Game of throne\"" );
        System.out.println("my favorite lesson is \"math\"");
        System.out.println("my favorite foreign language is \"spanish\"");
        // in order to print double code we need to use \"


        System.out.println("/ \\"); // in order to print one backward slash we need to give two backward slashes

        System.out.println("/ nkjhckjhkh \\");





    }
}
